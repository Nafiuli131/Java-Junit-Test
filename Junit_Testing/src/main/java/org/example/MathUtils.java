package org.example;

public class MathUtils {
    public int add(int a,int b){
        return a+b;
    }

    public int subtraction(int a,int b){
        return a-b;
    }

    public int divide(int a,int b){
        return a/b;
    }

    public int multiplication(int a,int b){
        return a*b;
    }
}
